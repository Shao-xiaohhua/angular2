export enum Operator {
    /** 等于. */
    Equals,
    /** 不等于. */
    NotEquals,
    /** 包含. */
    Contains,
    /** 在其中. */
    In,
    /** 不在其中. */
    NotIn,
    /** 小于. */
    Lt,
    /** 大于. */
    Gt,
    /** 小于等于. */
    Le,
    /** 大于等于. */
    Ge,
    /** 以..开始. */
    StartsWith,
    /** 以..结束. */
    EndsWith,
    /** 为空. */
    Null,
    /** 不为空. */
    NotNull
}
